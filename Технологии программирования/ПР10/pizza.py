class Пицца:
    def __init__(self):
        self.название = "Заготовка"
        self.тесто = "тонкое"
        self.соус = "кетчуп"
        self.начинка = []
        self.цена = 0

    def __str__(self):
        начинка_стр = ", ".join(self.начинка)
        return f"Пицца: {self.название} | Цена: {self.цена:.2f} р.\nТесто: {self.тесто} Соус: {self.соус}\nНачинка: {начинка_стр}"

    def подготовить(self):
        print(f"Начинаю готовить пиццу {self.название}")
        print(f"   - замешиваю {self.тесто} тесто...")
        print(f"   - добавляю соус: {self.соус}...")
        начинка_стр = ", ".join(self.начинка)
        print(f"   - и, конечно: {начинка_стр}...")

    def испечь(self):
        print("Выпекаю пиццу... Готово!")

    def нарезать(self):
        print("Нарезаю на аппетитные кусочки...")

    def упаковать(self):
        print("Упаковываю в фирменную упаковку и готово!")

